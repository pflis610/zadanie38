let liczba1 = 1;
let liczba2 = 2;
if (liczba1+liczba2 >= 0) {
    console.log('Wynik dodawania wynosi '+ (liczba1+liczba2));
} else {
    console.log('Wynik jest nieprawidłowy')
}
if (liczba1-liczba2 >= 0) {
    console.log('Wynik odejmowania wynosi ' + (liczba1-liczba2));
} else {
    console.log('Wynik jest nieprawidłowy');
}
if (liczba1*liczba2 >= 0) {
    console.log('Wynik mnożenia wynosi ' + (liczba1*liczba2));
} else {
    console.log('Wynik jest nieprawidłowy');
}